        static Form FormularioPadre;
        public frmRegistroVideo(string usuario, Form formularioPadre)
        {
            InitializeComponent();
            UsuarioAplicacion = usuario;
            navegador1.Usuario = UsuarioAplicacion;
            FormularioPadre = formularioPadre;
        }

        public void procLlenarCmb(string Tabla, string Campo, ComboBox CmbAgregar)
        {
            string[] Items = cn.funcItems(Tabla, Campo);
            for (int I = 0; I < Items.Length; I++)
            {
                if (Items[I] != null)
                {
                    if (Items[I] != "")
                    {
                        CmbAgregar.Items.Add(Items[I]);
                    }
                }
            }
        }

        private void frmRegistroVideo_Load(object sender, EventArgs e)
        {
            procLlenarCmb("generos", "idGenero", cmbGeneroId);
            procLlenarCmb("generos", "nombreGenero", cmbGenero);
        }

        private void navegador1_Load(object sender, EventArgs e)
        {
            List<string> CamposTabla = new List<string>();
            List<Control> lista = new List<Control>();
            navegador1.aplicacion = 1306;
            navegador1.tbl = "registrovideo";
            navegador1.campoEstado = "estado";
            navegador1.MDIformulario = FormularioPadre;
            foreach (Control C in this.Controls)
            {
                if ((C.Tag != null) && (!C.Tag.ToString().Equals("")))
                {
                    if (C is TextBox)
                    {
                        lista.Add(C);

                    }
                    else if (C is ComboBox)
                    {
                        lista.Add(C);

                    }
                    else if (C is DateTimePicker)
                    {
                        lista.Add(C);
                    }
                }
            }
            navegador1.control = lista;
            navegador1.formulario = this;
            navegador1.DatosActualizar = dataGridView1;
            navegador1.procActualizarData();
            navegador1.procCargar();
            navegador1.ayudaRuta = "";
            navegador1.ruta = "";
        }

        private void cmbGenero_SelectedIndexChanged(object sender, EventArgs e)
        {
            cmbGeneroId.SelectedIndex = cmbGenero.SelectedIndex;
        }
